     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'patient_name'=>'required',
            'patient_number'=>'required',
            'reservation_date'=>'required'


        ]);

        $saveData = new Reservation();

        $saveData->patient_name = $request['patient_name'];
